package com.company;

public class TiposPrimitivos {
    public static void main(String[] args) {
        String variavelString = ""; // o tipo String não possui um tipo primitivo equivalente.

        byte variavelByte = 1;  // o tipo byte, assim como outros primitivos,
                                // não pode manter um estado de ausência de valor.
                                // Lembrando que ausência de valor é representado pela palavra null
                                // que significa nulo.

        short variavelshort = 1;

        int variavelInteger = 1; // o tipo normal de int é Integer.

        long variavelLong = 1L;

        float variavelfloat = 1.0f;

        double variaveldouble = 1.0;

        char variavelCharacter = 'A'; // o tipo normal de char é Character.

        boolean variavelBoolean = true;

    }
}
